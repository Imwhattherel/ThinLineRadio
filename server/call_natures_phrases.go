// misleadingCallNaturePhrase reports phrases that must not stay attached to a
// label (learned or seeded). Bare "ALARM DROP" on medical steals police/ATM
// tamper alarms into ALARM DROP MEDICAL.
func misleadingCallNaturePhrase(label, phrase string) bool {
	label = strings.ToUpper(strings.TrimSpace(label))
	phrase = strings.ToUpper(strings.TrimSpace(phrase))
	if label == "ALARM DROP MEDICAL" && (phrase == "ALARM DROP" || phrase == "ALARM") {
		return true
	}
	return false
}

